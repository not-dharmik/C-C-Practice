#include<iostream>
using namespace std;
int main(){
    int a;
    cout<<"Size of int is: "<<sizeof(a)<<endl;
    
    char b;
    cout<<"Size of char is: "<<sizeof(b)<<endl;
    
    float c;
    cout<<"Size of float is: "<<sizeof(c)<<endl;
    
    bool d;
    cout<<"Size of bool is: "<<sizeof(d)<<endl;
    
    
    return 0;
}